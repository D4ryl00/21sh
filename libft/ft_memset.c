#include <string.h>
#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*str;

	i = -1;
	str = (char *)b;
	while (++i < len)
		str[i] = (unsigned char)c;
	return (b);
}
