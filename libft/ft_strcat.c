#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;

	i = -1;
	while (s1[++i])
		;
	while (*s2)
		s1[i++] = *s2++;
	s1[i] = '\0';
	return (s1);
}
